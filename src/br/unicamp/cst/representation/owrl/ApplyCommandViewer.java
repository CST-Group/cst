    private void jButtonApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApplyActionPerformed
        
        String s = jTACommand.getText();
       jTACommand.setText(null);
       double value = 0.0;
       
        System.out.println(s);
        
        String[] tokens = s.split("/"); //Command | name | value | nodeNumber
        
        String command= tokens[0].toString();
        String name= tokens[1].toString();
        if(!(tokens[2].toString().equals(""))){
            value= Double.parseDouble(tokens[2].toString());
        }
        
        int nodeNumber = Integer.parseInt(tokens[3].toString());
        
        executeCommand( command, name, value, nodeNumber);

        
    }//GEN-LAST:event_jButtonApplyActionPerformed

    
    private void executeCommand(String command, String name, double value, int nodeNumber ) {

        switch (command) {
            case "createObject":

                AbstractObject obj = new AbstractObject(name);
                jTree = TreeManager.newAddNodeJTree(obj, jSPTree, jTree, nodeNumber);
                jTree.setCellRenderer(new RendererJTree());
                jSPTree.setViewportView(jTree);

                break;
            case "createProperty":
                
                Property prop = new Property(name);
                jTree = TreeManager.newAddNodeJTree(prop, jSPTree, jTree, nodeNumber);
                jTree.setCellRenderer(new RendererJTree());
                jSPTree.setViewportView(jTree);                                

                break;
             case "createQualityDimension":
                 
                QualityDimension qualityDim = new QualityDimension(name, value);
                jTree = TreeManager.newAddNodeJTree(qualityDim, jSPTree, jTree, nodeNumber);
                jTree.setCellRenderer(new RendererJTree());
                jSPTree.setViewportView(jTree); 

                break;    
            default:
                System.out.println("Comamnd doesn't exist!");
        }

    }
    
    
